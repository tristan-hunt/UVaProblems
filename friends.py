import sys


def load():
	num_cases = int(next(sys.stdin))
	for i in range(num_cases):
		line = next(sys.stdin).split()
		N = int(line[0])
		M = int(line[1])
		friends = list()
		for j in range(M):
			pair = next(sys.stdin).split()
			a = int(pair[0])
			b = int(pair[1])
			friends.append((a,b))
		yield(N, M, friends)

